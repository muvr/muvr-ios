import UIKit

///
/// the Muvr theme colors
///
struct MRColor {

    static var darkBlue: UIColor {
        return UIColor(colorLiteralRed: 15.0/255, green: 85.0/255, blue: 134.0/255, alpha: 1)
    }
    
    static var lightBlue: UIColor {
        return UIColor(colorLiteralRed: 0, green: 156.0/255, blue: 224.0/255, alpha: 1)
    }
    
    static var black: UIColor {
        return UIColor(colorLiteralRed: 34.0/255, green: 34.0/255, blue: 34.0/255, alpha: 1)
    }
    
    static var green: UIColor {
        return UIColor(colorLiteralRed: 63.0/255, green: 204.0/255, blue: 58.0/255, alpha: 1)
    }
    
    static var orange: UIColor {
        return UIColor.orange()
    }
    
    static var gray: UIColor {
        return UIColor.gray()
    }
    
}
